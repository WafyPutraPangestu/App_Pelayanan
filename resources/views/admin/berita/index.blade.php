    <script>
        function beritaManager() {
            return {
                showToast: false,
                toastMessage: '',
                showDeleteModal: false,
                deleteUrl: '',

                init() {
                    // Check for flash messages
                    @if(session('success'))
                        this.showToastMessage('{{ session('success') }}');
                    @endif
                },

                showToastMessage(message) {
                    this.toastMessage = message;
                    this.showToast = true;
                    setTimeout(() => {
                        this.showToast = false;
                    }, 5000);
                },

                confirmDelete(url) {
                    this.deleteUrl = url;
                    this.showDeleteModal = true;
                }
            }
        }
    </script>
